#include "libft.h"

char		*ft_strdup(const char *src)
{
	int		len;
	char	*p;
	int		i;

	len = ft_strlen((char *)src);
	if ((p = (char *)malloc(sizeof(char) * (len + 1))) == NULL)
		return (0);
	i = 0;
	while (src[i] != '\0')
	{
		p[i] = src[i];
		i++;
	}
	p[i] = '\0';
	return (p);
}
